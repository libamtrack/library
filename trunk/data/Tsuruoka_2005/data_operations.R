############ read data from files #########################
AT.survival.data.read	<-	function(	){

# read gamma data
	SurvTable <- read.table("Survival-photons.dat")
 SurvTable$radiation = "gamma"
	SurvTable$Z = 0
	SurvTable$beta = 0
	SurvTable$zzbb = 0
	SurvTable$LET = 0
	SurvTable$Init.Energy = 0
	
# read ion data
	tmp.SurvTable <- read.table("Survival-ions.dat")
	tmp.SurvTable$radiation = "ion"
	SurvTable <- rbind( SurvTable , tmp.SurvTable )

 return (SurvTable)
}